				   selected : false,
				},
				virtualPay: false,
			}
		},
		components: {
			wPicker,
			xiaEditor,
			multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			if(options.virtualPay){
				this.virtualPay = true
			}
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.guanlizhanghao = this.user.guanlizhanghao
			this.ro.guanlizhanghao = true;
			this.ruleForm.guanlixingming = this.user.guanlixingming
			this.ro.guanlixingming = true;



			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`saishihuodong`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='saishimingcheng'){
						this.ruleForm.saishimingcheng = obj[o];
						this.ro.saishimingcheng = true;
						continue;
					}
					if(o=='fengmian'){
						this.ruleForm.fengmian = obj[o].split(",")[0];
						this.ro.fengmian = true;
						continue;
					}
					if(o=='saishididian'){
						this.ruleForm.saishididian = obj[o];
						this.ro.saishididian = true;
						continue;
					}
					if(o=='huodongshijian'){
						this.ruleForm.huodongshijian = obj[o];
						this.ro.huodongshijian = true;
						continue;
					}
					if(o=='faburen'){
						this.ruleForm.faburen = obj[o];
						this.ro.faburen = true;
						continue;
					}
					if(o=='baomingfei'){
						this.ruleForm.baomingfei = obj[o];
						this.ro.baomingfei = true;
						continue;
					}
					if(o=='baomingyaoqiu'){
						this.ruleForm.baomingyaoqiu = obj[o];
						this.ro.baomingyaoqiu = true;
						continue;
					}
					if(o=='saishineirong'){
						this.ruleForm.saishineirong = obj[o];
						this.ro.saishineirong = true;
						continue;
					}
					if(o=='guanlizhanghao'){
						this.ruleForm.guanlizhanghao = obj[o];
						this.ro.guanlizhanghao = true;
						continue;
					}
					if(o=='guanlixingming'){
						this.ruleForm.guanlixingming = obj[o];
						this.ro.guanlixingming = true;
						continue;
					}
					if(o=='discussnum'){
						this.ruleForm.discussnum = obj[o];
						this.ro.discussnum = true;
						continue;
					}
					if(o=='price'){
						this.ruleForm.price = obj[o];
						this.ro.price = true;
						continue;
					}
					if(o=='storeupnum'){
						this.ruleForm.storeupnum = obj[o];
						this.ro.storeupnum = true;
						continue;
					}
					if(o=='number'){
						this.ruleForm.number = obj[o];
						this.ro.number = true;
						continue;
					}
					if(o=='selected'){
						this.ruleForm.selected = obj[o];
						this.ro.selected = true;
						continue;
					}
