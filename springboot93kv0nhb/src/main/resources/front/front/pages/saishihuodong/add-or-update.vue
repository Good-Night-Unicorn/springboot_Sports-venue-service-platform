							await this.$api.update(`saishihuodong`, this.ruleForm);
						}else{
							oet = await this.$api.add(`saishihuodong`, this.ruleForm);
						}

						this.$utils.msgBack('提交成功');
					}
				} else {
				//跨表计算
					let oet = {}
					if(this.ruleForm.id){
						await this.$api.update(`saishihuodong`, this.ruleForm);
					}else{
						oet = await this.$api.add(`saishihuodong`, this.ruleForm);
					}

					this.$utils.msgBack('提交成功');
				}
