	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				saishimingcheng: '',
				fengmian: '',
				saishididian: '',
				huodongshijian: '',
				faburen: '',
				baomingfei: '',
				baomingyaoqiu: '',
				saishineirong: '',
				guanlizhanghao: '',
				guanlixingming: '',
				discussnum: '',
				price: '',
				storeupnum: '',
				number: '',
				selected: '',
				},
				// 登录用户信息
